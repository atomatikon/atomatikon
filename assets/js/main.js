document.getElementById('commentForm').addEventListener('submit', function(event) {
    event.preventDefault();

    var formData = new FormData(this);
    fetch('/api/comment', {
        method: 'POST',
        body: formData
    })
    .then(response => {
        if (response.ok) {
            document.getElementById('message').innerHTML = "Comment successfully posted and sent to moderation.";
            document.getElementById('commentForm').reset(); // Reset form fields
        } else {
            document.getElementById('message').innerHTML = "Failed to submit comment.";
        }
    })
    .catch(() => {
        document.getElementById('message').innerHTML = "Error posting comment.";
    });
});